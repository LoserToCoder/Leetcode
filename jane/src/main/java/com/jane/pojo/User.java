package com.jane.pojo;
public class User {
    private Integer id;
    private String username;
    private String password;
    private Integer gender;
    private String address;
    private String email;
    private String phone;
    private String qrcode;
    private String backgroundCover;
    private String avatar;
    private String score;

    private Integer status;


    private String reisterTime;

    public String getReisterTime() {
        return reisterTime;
    }

    public void setReisterTime(String reisterTime) {
        this.reisterTime = reisterTime;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public Integer getId() {
        return id;
    }
    public void setId(Integer id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public Integer getGender() {
        return gender;
    }

    public void setGender(Integer gender) {
        this.gender = gender;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getQrcode() {
        return qrcode;
    }

    public void setQrcode(String qrcode) {
        this.qrcode = qrcode;
    }

    public String getBackgroundCover() {
        return backgroundCover;
    }

    public void setBackgroundCover(String backgroundCover) {
        this.backgroundCover = backgroundCover;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }
}
